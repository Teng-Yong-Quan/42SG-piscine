char	*ft_strcpy(char *dest, char *src)
{
	char	*new_dest;

	new_dest = dest;
	while (*src != '\0')
	{
		*new_dest = *src;
		new_dest++;
		src++;
	}
	*new_dest = '\0';
	return (dest);
}
