int	ft_str_is_numeric(char *str)
{
	int	return_int;

	return_int = 1;
	while (*str != '\0')
	{
		if (!('0' <= *str && *str <= '9'))
		{
			return_int = 0;
			break ;
		}
		str++;
	}
	return (return_int);
}
