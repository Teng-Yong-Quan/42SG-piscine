char	*ft_strupcase(char *str)
{
	char	*ptr;

	ptr = str;
	while (*ptr != '\0')
	{
		if ('a' <= *ptr && *ptr <= 'z')
		{
			*ptr = *ptr + ('A' - 'a');
		}
		ptr++;
	}
	return (str);
}
